package day04_dataCasting;

public class C05_IncrementDecrement {

    public static void main(String[] args) {

        int num1 = 4;


        num1*=11;
        System.out.println("num1 = " + num1); // 44

        num1+= 4;
        System.out.println("num1 = " + num1); // 48

        num1 /=2;
        System.out.println("num1 = " + num1); // 24

        num1 ++;
        System.out.println("num1 = " + num1); // 25

        num1 --;
        System.out.println("num1 = " + num1); // 24



        // kalici olarak assing ettigimiz icin


    }
}
